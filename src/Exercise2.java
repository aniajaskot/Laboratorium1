public class Exercise2 {
    public static void main(String[] args) {
        double a = 12;
        double b = 5.5;

        double sum = a + b;
        double sub = a - b;
        double multi = a * b;
        double div = a / b;

        System.out.println(a + "+" + b + "=" + sum);
        System.out.println(a + "-" + b + "=" + sub);
        System.out.println(a + "*" + b + "=" + multi);
        System.out.println(a + "/" + b + "=" + div);
    }
}

